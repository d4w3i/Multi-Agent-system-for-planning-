# HELPER FUNCTIONS

def sanitize_for_triple_quotes(content: str, delimiter: str = "'''") -> str:
    """
    Sanitize content to prevent conflicts with string delimiters.
    Needed because generated project_info.py wraps README/tree content in
    triple-quoted strings; unescaped triple quotes inside would cause SyntaxError.

    Args:
        content: The content to sanitize
        delimiter: The delimiter that will be used (''' or \"\"\")

    Returns:
        Sanitized content with escaped delimiters
    """
    if delimiter == "'''":
        return content.replace("'''", r"\'\'\'")
    elif delimiter == '"""':
        return content.replace('"""', r'\"\"\"')
    return content

def find_prs_with_base_project(dataset_dir: Path) -> list[Path]:
    """
    Find all PRs that have a base_project/ folder.

    Args:
        dataset_dir: PR4Code dataset directory

    Returns:
        List of Paths to PR directories with base_project/
    """
    pr_dirs = []

    # Pattern: */pr_*/ finds all subdirectories starting with "pr_"
    for pr_dir in sorted(dataset_dir.glob('*/pr_*/')):
        base_project = pr_dir / 'base_project'
        if base_project.exists() and base_project.is_dir():
            pr_dirs.append(pr_dir)

    return pr_dirs

