# main() FUNCTION - CLI Entry Point

def main():
    """
    Entry point for command-line execution.

    USAGE:
        python -m context_retrieving.batch_context_retriever <dataset_dir> [options]

    OPTIONS:
        --limit N         Process only N PRs
        --skip-existing   Skip PRs with existing context_output/
        --no-masca        Disable Masca LLM analysis (enabled by default)
    """

    # ArgumentParser configuration
    parser = argparse.ArgumentParser(
        description="Batch context retrieval for PR4Code base projects"
    )

    parser.add_argument(
        'dataset_dir',
        help='PR4Code dataset directory (e.g., PR4Code/dataset_pr_commits_py/)'
    )

    parser.add_argument(
        '--limit',
        type=int,
        default=None,
        help='Limit number of PRs to process (for testing)'
    )

    parser.add_argument(
        '--skip-existing',
        action='store_true',
        help='Skip PRs that already have context_output/'
    )

    parser.add_argument(
        '--no-masca',
        action='store_true',
        help='Disable Masca LLM analysis (enabled by default; requires OPENAI_API_KEY)'
    )

    args = parser.parse_args()

    # Input validation
    dataset_dir = Path(args.dataset_dir)

    if not dataset_dir.exists():
        log_error(f"Directory not found: {dataset_dir}")
        sys.exit(1)

    # Find PRs with base_project/
    logger.info(f"Searching for PRs with base_project/ in {dataset_dir}...")
    pr_dirs = find_prs_with_base_project(dataset_dir)

    if not pr_dirs:
        log_error("No PRs with base_project/ found")
        sys.exit(1)

    logger.info(f"Found {len(pr_dirs)} PRs with base_project/")

    # Apply filters

    # Filter already processed PRs
    if args.skip_existing:
        original_count = len(pr_dirs)
        pr_dirs = [
            d for d in pr_dirs
            if not (d / 'context_output' / 'call_graph.json').exists()
        ]
        skipped = original_count - len(pr_dirs)
        if skipped > 0:
            logger.info(f"Skipped {skipped} already processed PRs")

    # Apply limit
    if args.limit:
        pr_dirs = pr_dirs[:args.limit]

    if not pr_dirs:
        logger.info("No PRs to process")
        sys.exit(0)

    # Log configuration
    logger.info(f"Processing {len(pr_dirs)} PRs...")
    if args.no_masca:
        logger.info("Masca analysis disabled (--no-masca)")

    # Execution
    retriever = BatchContextRetriever(with_masca=not args.no_masca)

    success_count = 0
    failed_count = 0
    masca_results = []

    # Use tqdm if available
    iterator = tqdm(pr_dirs, desc="Processing PRs") if HAS_TQDM else pr_dirs

    for pr_dir in iterator:
        success, masca_data = retriever.process_pr(pr_dir)

        if success:
            success_count += 1
        else:
            failed_count += 1

        if masca_data is not None:
            pr_id = f"{pr_dir.parent.name}/{pr_dir.name}"
            masca_results.append({
                "pr_id": pr_id,
                "system_prompt": masca_data["system_prompt"],
                "prompt": masca_data["prompt"],
                "output": masca_data["output"],
                "input_tokens": masca_data["input_tokens"],
                "output_tokens": masca_data["output_tokens"],
                "total_tokens": masca_data["total_tokens"],
            })

    # Write MASCA token usage JSON if any results were collected
    if masca_results:
        token_json_path = dataset_dir / "masca_output_token.json"
        with open(token_json_path, "w", encoding="utf-8") as f:
            json.dump(masca_results, f, indent=2, ensure_ascii=False)
        log_success(f"MASCA token usage saved to {token_json_path}")

    # Summary
    print()
    print("=" * 60)
    log_success("Context retrieval completed!")
    print(f"  Success: {success_count}")
    print(f"  Failed:  {failed_count}")
    print("=" * 60)

if __name__ == '__main__':
    main()
