"""
=============================================================================
BATCH_CONTEXT_RETRIEVER.PY - Context Retrieval for PR4Code Base Projects
=============================================================================

This module applies the context retrieval process (call graph + context
files + Masca analysis) in batch to PR4Code dataset base projects.

PURPOSE:

PR4Code contains ~949 PRs with `base_project/` folders representing
the entire repository BEFORE the PR changes. This script processes
each base_project to generate:
1. Complete call graph (JSON + DOT)
2. ASCII directory tree
3. Context files for each function
4. (Optional) Masca analysis with LLM

INPUT STRUCTURE (PR4Code):

    PR4Code/dataset_pr_commits_py/
    └── owner_repo/
        └── pr_123/
            ├── data.json           # PR metadata
            ├── ground_truth.json   # Already generated
            ├── base_project/       # ← INPUT: Complete project
            ├── original_files/     # Pre-modification files
            └── modified_files/     # Post-modification files

OUTPUT STRUCTURE:

    pr_123/
    └── context_output/             # ← OUTPUT: Generated by this script
        ├── call_graph.json         # Complete call graph
        ├── project_tree.txt        # ASCII directory tree
        ├── project_info.py         # DIRECTORY_TREE + README + MASCA
        ├── masca_analysis.md       # Only if --with-masca
        └── context_files/          # Hierarchical context files
            └── [structure mirroring base_project/]

project_info.py CONTENT:

    The project_info.py file contains three string variables:
    - DIRECTORY_TREE: ASCII tree representation of project structure
    - README: Content of the project's README file
    - MASCA: AI analysis of the project (empty if --with-masca not used)

    Usage example:
        from context_output.project_info import DIRECTORY_TREE, README, MASCA

        print(DIRECTORY_TREE)  # File structure
        print(README)          # Documentation
        print(MASCA)           # AI analysis (if available)

=============================================================================
USAGE GUIDE
=============================================================================

BASIC COMMANDS:
---------------

    # Process all PRs with base_project (with MASCA analysis by default)
    python -m context_retrieving.batch_context_retriever PR4Code/dataset_pr_commits_py/

    # Process without MASCA analysis
    python -m context_retrieving.batch_context_retriever PR4Code/dataset_pr_commits_py/ --no-masca


AVAILABLE OPTIONS:
------------------

    --limit N         Process only the first N PRs (for testing)
    --skip-existing   Skip PRs that already have context_output/
    --no-masca        Disable AI analysis with Masca (enabled by default;
                      requires OPENAI_API_KEY in .env file)


EXAMPLES:
---------

    # 1. Quick test on 5 PRs (MASCA enabled by default)
    python -m context_retrieving.batch_context_retriever PR4Code/dataset_pr_commits_py/ --limit 5

    # 2. Quick test without MASCA
    python -m context_retrieving.batch_context_retriever PR4Code/dataset_pr_commits_py/ --no-masca --limit 10

    # 3. Resume interrupted processing (skip existing)
    python -m context_retrieving.batch_context_retriever PR4Code/dataset_pr_commits_py/ --skip-existing

    # 4. Process all without MASCA, skipping existing
    python -m context_retrieving.batch_context_retriever PR4Code/dataset_pr_commits_py/ --no-masca --skip-existing

    # 5. Process a single repository
    python -m context_retrieving.batch_context_retriever PR4Code/dataset_pr_commits_py/1Panel-dev_MaxKB/


NOTES:
------
    - MASCA is enabled by default; use --no-masca to skip it
    - MASCA requires OPENAI_API_KEY and makes one API call per PR (~$0.001/PR)
    - The --skip-existing flag checks for the presence of call_graph.json
=============================================================================
"""
