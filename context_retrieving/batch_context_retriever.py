# BatchContextRetriever CLASS

class BatchContextRetriever:
    """
    Process batches of base_project/ to generate context files.

    This class coordinates the entire context retrieval process:
    1. Call graph analysis with CallGraphBuilder
    2. Directory tree generation with TreeGenerator
    3. Context files generation with ContextGenerator
    4. (Optional) Masca analysis with LLM

    Attributes:
        with_masca (bool): If True, also runs Masca analysis

    Example:
        retriever = BatchContextRetriever()
        success = retriever.process_pr(Path("PR4Code/.../pr_123"))
    """

    def __init__(self, with_masca: bool = True):
        """
        Initialize the retriever.

        Args:
            with_masca: If True, include Masca LLM analysis (requires API key)
        """
        self.with_masca = with_masca

        # Lazy import of Masca runner only if needed
        if with_masca:
            try:
                from GenAI.masca_runner import run_masca_analysis, save_masca_output
                self._run_masca = run_masca_analysis
                self._save_masca = save_masca_output
            except ImportError:
                log_warning("Masca runner not available. --with-masca disabled.")
                self.with_masca = False

    def process_pr(self, pr_dir: Path) -> tuple[bool, dict | None]:
        """
        Process a single PR.

        Steps:
        1. Verify that base_project/ exists
        2. Create context_output/ directory
        3. Run CallGraphBuilder on base_project/
        4. Export call_graph.json
        5. Generate project_tree.txt with TreeGenerator
        6. Generate project_info.py with README and tree
        7. Generate context_files/ with ContextGenerator
        8. (Optional) Run Masca analysis

        Args:
            pr_dir: Path to the PR directory

        Returns:
            Tuple of (success: bool, masca_result: dict | None)
        """
        base_project = pr_dir / 'base_project'
        output_dir = pr_dir / 'context_output'

        # Step 1: Verify input
        if not base_project.exists():
            log_warning(f"base_project/ not found in {pr_dir.name}")
            return False, None

        try:
            # Step 2: Create output directory
            output_dir.mkdir(exist_ok=True)
            context_files_dir = output_dir / 'context_files'
            context_files_dir.mkdir(exist_ok=True)

            # Step 3: Build call graph
            logger.info(f"Building call graph for {pr_dir.name}...")
            builder = CallGraphBuilder(verbose=False)
            call_graph = builder.analyze_repository(str(base_project))

            # Step 4: Export call graph
            builder.to_json(str(output_dir / 'call_graph.json'))

            # Step 5: Generate tree
            tree_gen = TreeGenerator(
                root_path=str(base_project),
                max_depth=4,
                show_hidden=False
            )
            lines, _ = tree_gen.generate()
            tree_content = '\n'.join(lines)
            (output_dir / 'project_tree.txt').write_text(tree_content, encoding='utf-8')

            # Step 6: Load README
            readme_content = self._load_readme(base_project)

            # Step 7: Masca analysis (optional, but done BEFORE project_info)
            masca_content = ""
            masca_result = None
            if self.with_masca:
                try:
                    logger.info(f"Running Masca analysis for {pr_dir.name}...")
                    masca_result = self._run_masca(readme_content, tree_content)
                    masca_content = masca_result["output"]
                    # Also save to separate file
                    self._save_masca(
                        masca_content,
                        str(output_dir / 'masca_analysis.md'),
                        tree_content
                    )
                except Exception as e:
                    log_warning(f"Masca analysis failed for {pr_dir.name}: {e}")
                    masca_content = f"# Masca Analysis Failed\n\nError: {str(e)}"

            # Step 8: Generate project_info.py with DIRECTORY_TREE, README and MASCA
            self._generate_project_info(output_dir, tree_content, readme_content, masca_content)

            # Step 9: Generate context files
            if call_graph:  # Only if there are functions
                generator = ContextGenerator(
                    call_graph,
                    repo_root=str(base_project),
                    verbose=False
                )
                generator.generate_all_context_files(str(context_files_dir))

            return True, masca_result

        except Exception as e:
            log_error(f"Error processing {pr_dir.name}: {e}")
            return False, None

    def _load_readme(self, base_project: Path) -> str:
        """
        Load the README from base_project.

        Searches in order: README.md, README.rst, README.txt, README

        Args:
            base_project: Path to the base project

        Returns:
            README content or empty string if not found
        """
        readme_names = ['README.md', 'README.rst', 'README.txt', 'README']

        for name in readme_names:
            readme_path = base_project / name
            if readme_path.exists():
                # Best-effort: try UTF-8 first, then Latin-1. If both fail,
                # skip this README and return empty string below.
                try:
                    return readme_path.read_text(encoding='utf-8')
                except Exception:
                    try:
                        return readme_path.read_text(encoding='latin-1')
                    except Exception:
                        pass

        return ""

    def _generate_project_info(
        self,
        output_dir: Path,
        tree_content: str,
        readme_content: str,
        masca_content: str = ""
    ):
        """
        Generate project_info.py with DIRECTORY_TREE, README and MASCA.

        Args:
            output_dir: Output directory
            tree_content: Directory tree content
            readme_content: README content
            masca_content: MASCA analysis content (optional)
        """
        project_info_path = output_dir / 'project_info.py'

        with open(project_info_path, 'w', encoding='utf-8') as f:
            f.write('"""Project Information - Auto-generated file"""\n\n')

            # DIRECTORY_TREE variable
            f.write("DIRECTORY_TREE = '''")
            f.write(sanitize_for_triple_quotes(tree_content, "'''"))
            f.write("'''\n\n")

            # README variable
            f.write("README = '''")
            f.write(sanitize_for_triple_quotes(readme_content, "'''"))
            f.write("'''\n\n")

            # MASCA variable (AI analysis of the project)
            f.write("MASCA = '''")
            f.write(sanitize_for_triple_quotes(masca_content, "'''"))
            f.write("'''\n")

